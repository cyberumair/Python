# Arimthmetic Operators : +, -, *, /, **, % etc

a = 5
b = 6
c = a + b
print(c)

# Assignement Operators : =. +=, -=, *=, **=

d = 4-5 # Assigns 4-5 to d
print(d)

# b += 3 # Increment value of b by 3 and then assigns it to b
b -= 3 # Decrement value of b by 3 and then assign it to b
print(b)

# Comparision Operators (Always returns Boolean value (True or False) : ==, <, >, <=, >=, != etc

e = 4<5
print(e)

# Logical Operators : or, not, and

# Truth Table of 'or'
print("True or False is", True or False)
print("False or True is", False or True)
print("True or True is", True or True)
print("False or False is", False or False)

# Truth Table of 'and'
print("True and False is", True and False)
print("False and True is", False and True)
print("True and True is", True and True)
print("False and False is", False and False)

print(not(False))
