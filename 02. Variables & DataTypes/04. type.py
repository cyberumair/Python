# type() funtion is used to print type of any variable

a = 0
print(type(a)) # <class 'int'> (Integer)

f = 31.5
print(type(f)) # <class 'float'> (Floating Number)

s = "30"
print(type(s)) # <class 'str'> (String)

# Type Conversion

# float() function is used to convert any type of variable to floating number

b = float(a)
print(b) # is equal to a but as a floating number

# str() function is used to convert any type of variable to a string

st = str(b)
print(type(st))

# Similarly , int() , bool() are used to convert any type of variable to integer or boolean respectively
